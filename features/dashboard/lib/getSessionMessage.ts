function pick(phrases: string[]): string {
    return phrases[Math.floor(Math.random() * phrases.length)];
}

export function getSessionMessage(count: number): string {
    switch (true) {
        case count === 0:
            return pick([
                "היום אין פגישות. קח רגע לעצמך, אתה עושה עבודה חשובה.",
                "יום ללא פגישות — הזדמנות לנוח, להתארגן ולחזור בכוחות חדשים.",
                "אין פגישות היום. תן לעצמך רגע שקט ותמלא מצברים.",
                "היום פנוי מפגישות. קח את הזמן ועשה מה שמטעין אותך.",
                "אין פגישות בלוח הזמנים. יום כזה הוא מתנה — נצל אותו.",
            ]);
        case count === 1:
            return pick([
                "יש לך פגישה אחת היום. יום שקט — תהנה מהקצב הנוח.",
                "פגישה אחת בלבד היום. יש לך מרחב לחשוב ולהתכונן היטב.",
                "יום עדין עם פגישה אחת. תרוץ בטמפו שנוח לך.",
                "רק פגישה אחת מחכה לך היום — תוכל להתמסר לה לחלוטין.",
                "פגישה אחת היום. יש לך הרבה שקט סביבה — תשתמש בו.",
            ]);
        case count <= 4 && count >= 2:
            return pick([
                `${count} פגישות היום. קצב בריא, יש לך זמן לנשום בין לבין.`,
                `${count} פגישות מחכות לך. יום מאוזן — לא עמוס, לא ריק.`,
                `${count} פגישות היום. מספיק כדי להרגיש פרודוקטיבי, לא יותר מדי.`,
                `יש לך ${count} פגישות היום. קצב טוב — תיהנה מהזרימה.`,
                `${count} פגישות בתוכנית. יום שפוי עם מקום לנשימה.`,
            ]);
        case count <= 7 && count >= 5:
            return pick([
                `${count} פגישות מחכות לך היום. יום עמוס — זכור לשמור על עצמך גם בתוך העומס.`,
                `${count} פגישות היום. יום מלא — שים לב לפסקות קצרות בין פגישה לפגישה.`,
                `${count} פגישות בלוח. לוח זמנים עמוס — תדאג לרגעי עצירה קצרים.`,
                `יש לך ${count} פגישות היום. יום דחוס — שתה מים, קח אוויר.`,
                `${count} פגישות מחכות. יום אינטנסיבי — נסה לשמור על אנרגיה לאורך כולו.`,
            ]);
        default:
            return pick([
                `${count} פגישות היום — יום אינטנסיבי. אתה מדהים, אבל אל תשכח לנוח בסוף היום.`,
                `${count} פגישות בתוכנית. יום כבד — תזכור שאתה לא מכונה, תן לעצמך חסד.`,
                `${count} פגישות היום. עומס אמיתי — הקפד על פסקה בין לבין גם אם קצרה.`,
                `${count} פגישות מחכות לך. יום ארוך לפניך — שמור אנרגיה לאורך כל הדרך.`,
                `${count} פגישות היום — יום מאתגר. כשיגמר, תגמל את עצמך על המאמץ.`,
            ]);
    }
}
